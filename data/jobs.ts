export interface Job {
    /** ใช้เป็น URL: /careers/<slug> */
    slug: string
    /** ชื่อตำแหน่ง */
    title: string
    /** หมวดหมู่ทีม */
    subtitle: string
    /** ไอคอน Lucide */
    icon: LucideIcon
    /** Tailwind gradient classes (jobIcon background) */
    color: string
    /** Tailwind background tint สำหรับ left panel */
    bgColor: string
    /** Tailwind classes สำหรับ badge ประเภทงาน */
    badgeColor: string
    /** ประเภทการจ้าง — แสดงบนการ์ด */
    type: string
    /** สถานที่ปฏิบัติงาน */
    location: string
    /** Employment type สำหรับ JSON-LD (FULL_TIME, PART_TIME, CONTRACTOR, INTERN ฯลฯ) */
    employmentType:
        | "FULL_TIME"
        | "PART_TIME"
        | "CONTRACTOR"
        | "TEMPORARY"
        | "INTERN"
    /** สรุปสั้น แสดงในการ์ดและ meta description */
    summary: string
    /** เทคโนโลยี / Stack ที่จะได้ใช้ — แสดงเป็น badge บนหน้า detail (เฉพาะตำแหน่งสาย dev) */
    techStack?: string[]
    /** หน้าที่ความรับผิดชอบ */
    responsibilities: string[]
    /** คุณสมบัติผู้สมัคร */
    qualifications: string[]
    /** เอกสารที่ต้องใช้สมัครงาน (ระบุ "(ถ้ามี)" สำหรับเอกสารเสริม) */
    documents: string[]
    /** หมายเหตุเพิ่มเติมท้ายรายการเอกสาร — แสดงเป็นข้อความเล็ก ๆ ใต้ list */
    documentsNote?: string
}

